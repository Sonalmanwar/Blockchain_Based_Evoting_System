    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String name=jTextField1.getText();
        String fathername=jTextField2.getText();
        String age=jTextField3.getText();
        String sex="";
        if(jRadioButton1.isSelected())
            sex="Male";
        if(jRadioButton2.isSelected())
            sex="Female";
        if(jRadioButton3.isSelected())
            sex="Other";
        String dob=jTextField4.getText();
        String adharno=jTextField5.getText();
        String emailid=jTextField6.getText();
        String mobile=jTextField7.getText();
        String address=jTextArea1.getText();
        String state=jTextField8.getText();
        
         if(name.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Voter Name");
         else if(fathername.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Voter Father Name");
         else if(age.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Age");
         else if(sex.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Select Sex");
         else if(dob.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Date Of Birth");
         else if(!isValidAadharCard(adharno))
             JOptionPane.showMessageDialog(null, "Please Enter Valid Adhar Number");
         else if(!isValidEmail(emailid))
             JOptionPane.showMessageDialog(null,"Please Enter Valid Email ID");
         else if(!isValidMobile(mobile))
             JOptionPane.showMessageDialog(null, "Please Enter Valid Mobile Number");
         else if(address.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Enter Voter Address");
          else if(state.isEmpty())
             JOptionPane.showMessageDialog(null, "Please Select Your State");
         else
          {
              UpadateVoterInformation uvi=new UpadateVoterInformation();
              if(uvi.doUpdateVoter(name, fathername, age, sex, dob, adharno, emailid, mobile, address, state))
                  JOptionPane.showMessageDialog(null, "Data Updated Successfully");
              else
                  JOptionPane.showMessageDialog(null, "Error In Updation");
          }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

